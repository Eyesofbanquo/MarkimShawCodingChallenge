        let title = cell.viewWithTag(1) as! UILabel
        let author = cell.viewWithTag(2) as! UILabel
        
        title.text = self.favoriteBooks[indexPath.row].valueForKey("title") as? String
        author.text = self.favoriteBooks[indexPath.row].valueForKey("author") as? String
        // Configure the cell...

